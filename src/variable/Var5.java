package variable;


/**
 * 변수 초기화
 * 변수를 선언하고, 선언한 변수에 처음으로 값을 저장하는 것을 변수 초기화라 한다.
 * @author Dong-Joon Oh */

public class Var5 {
    public static void main(String[] args) {
        //1. 변수 선언, 초기화 각각 따로
        int a;
        a = 1;
        System.out.println(a);

        int b = 2; // 2. 변수 선언과 초기화를 한번에
        System.out.println(b);

    }
}
